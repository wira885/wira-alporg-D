a = 24
b = 21

#operasi kali X
hasil = a * b 
print("nilai dari a * b =",hasil)

#operasi bagi :
hasil = a / b 
print("nilai dari a / b =",hasil)

#operasi tambah +
hasil = a + b 
print("nilai dari a + b =",hasil)

#operasi kurang -
hasil = a - b 
print("nilai dari a - b=",hasil)

#operasi perpangkatan **
hasil = a ** b 
print("nilai dari a ** b=",hasil)

#modulus (sisa bagi) %
hasil = a % b 
print("nilai dari a mod b=",hasil)

#floor dividion //
hasil = a // b 
print("nilai dari a flor b=",hasil)